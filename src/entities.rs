/// HTML5 numeric-reference remap for the C1 range (`&#128;` \u{2192} \u{20AC},
/// etc.); code points outside this table resolve to themselves.
/// `(source, mapped)`, sorted by source. 27 entries.
static C1_REMAP: &[(u32, u32)] = &[
    (128, 8364), (130, 8218), (131, 402), (132, 8222),
    (133, 8230), (134, 8224), (135, 8225), (136, 710),
    (137, 8240), (138, 352), (139, 8249), (140, 338),
    (142, 381), (145, 8216), (146, 8217), (147, 8220),
    (148, 8221), (149, 8226), (150, 8211), (151, 8212),
    (152, 732), (153, 8482), (154, 353), (155, 8250),
    (156, 339), (158, 382), (159, 376),
];

/// Resolve a named entity (`copy`, `Alpha`, …) to its code point, or
/// `None` if it is not in kramdown's table (the caller then leaves the
/// `&` literal, matching kramdown's `&amp;name;` fallback).
pub(crate) fn named(name: &str) -> Option<u32> {
    NAMED.binary_search_by(|&(n, _)| n.cmp(name)).ok().map(|i| NAMED[i].1)
}

/// Apply the C1 numeric-reference remap; a code point not in the table is
/// returned unchanged.
pub(crate) fn remap_numeric(cp: u32) -> u32 {
    match C1_REMAP.binary_search_by(|&(s, _)| s.cmp(&cp)) {
        Ok(i) => C1_REMAP[i].1,
        Err(_) => cp,
    }
}

