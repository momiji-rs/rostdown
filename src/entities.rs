        assert!(
            NAMED.windows(2).all(|w| w[0].0 < w[1].0),
            "NAMED must be strictly sorted by name (Rust str ordering)"
        );
        assert!(C1_REMAP.windows(2).all(|w| w[0].0 < w[1].0));
